// Write a program that converts a temperature of 34 degrees Celsius to its Fahrenheit equivalent using the formula: F = 9 / 5 * C + 32

// The conversion of 34°C to Fahrenheit
console.log("34°C in Fahrenheit = " + String((9 / 5) * 34 + 32));

// Write a program that computes and outputs the area of a circle with a radius of 14 cm.  (A = Pi * r * r)

// The area of a circle with radius 14 cm
console.log("Area of circle with radius 14 cm = " + String(Math.PI * 14 * 14) + " cm²");

//Write a program that outputs the volume of a sphere with a radius of 4 cm. (V = 4 / 3 * Pi * r * r * r)

// The volume of a sphere with radius 4 cm
console.log("Volume of sphere with radius 4 cm = " + String((4 / 3) * Math.PI * 4 * 4 * 4) + " cm³");

